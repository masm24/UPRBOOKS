    <p>Sobre Nosotros</p>
    
    <div align="center">
     <p>Esta página web tiene como objetivo facilitar a los estudiantes de la Universidad de Puerto Rico en Ponce (UPRP) la venta y compra de libros de texto.
     El costo de los textos es considerablemente alto. Para los estudiantes de UPRP, tener acceso fácil a la compra de libros usados es importante debido al aumento significativo en los costos de la matrícula que reduce su presupuesto.
     La librería de la UPRP no tiene todos los libros de texto disponibles y el número de librerías en Ponce es limitado.
     La compra en línea de libros usados requiere disponer de una tarjeta de crédito y los libros tardan bastante tiempo en llegar a manos del comprador.
     Existen páginas de Facebook donde los estudiantes publican su intención de comprar o vender libros usados, pero estas carecen de una estructura que facilite la búsqueda por materia y la comparación de precios.
     Finalmente algunos estudiantes optan por publicar anuncios en los tablones de edicto de la Universidad. Pocos estudiantes leen estos anuncios y no es una manera efectiva de poner en contacto compradores y vendedores.
     Esta página web ofrece una mejor  alternativa para la compra y venta de libros de textos universitarios.</p>

